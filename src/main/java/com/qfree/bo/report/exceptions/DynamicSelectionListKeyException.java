package com.qfree.bo.report.exceptions;

public class DynamicSelectionListKeyException extends ReportingException {

	private static final long serialVersionUID = 1L;

	public DynamicSelectionListKeyException() {
	}

	public DynamicSelectionListKeyException(String message) {
		super(message);
	}

	public DynamicSelectionListKeyException(Throwable cause) {
		super(cause);
	}

	public DynamicSelectionListKeyException(String message, Throwable cause) {
		super(message, cause);
	}

	public DynamicSelectionListKeyException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
